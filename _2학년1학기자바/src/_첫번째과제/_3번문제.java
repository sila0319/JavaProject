package _첫번째과제;

public class _3번문제 {
	public static void main(String[] args) {
		double firstsum = 15.8;
		double secondsum = 3.7;
		
		System.out.printf("%.1f + %.1f = %.1f %n",firstsum,secondsum,firstsum+secondsum);
		System.out.printf("%.1f - %.1f = %.1f %n",firstsum,secondsum,firstsum-secondsum);
		System.out.printf("%.1f * %.1f = %.1f %n",firstsum,secondsum,firstsum*secondsum);
		System.out.printf("%.1f / %.1f = %.1f %n",firstsum,secondsum,firstsum/secondsum);
	}
}
