package _첫번째과제;

public class _6번문제 {
	public static void main(String[] args) {
		
		double firstsum = 10.8;
		double secondsum = 3;
		
		System.out.printf("%.1f + %.0f = %.1f %n",firstsum,secondsum,firstsum+secondsum);
		System.out.printf("%.1f - %.0f = %.1f %n",firstsum,secondsum,firstsum-secondsum);
		System.out.printf("%.1f / %.0f = %.1f %n",firstsum,secondsum,firstsum*secondsum);
		System.out.printf("%.1f * %.0f = %.1f %n",firstsum,secondsum,firstsum/secondsum);
	}

}
