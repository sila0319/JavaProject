package _첫번째과제;

public class _1번문제 {
	public static void main(String[] args) {
		int a =20;
		int b =3;
		System.out.println("20 + 3 = "+ (a+b));
		System.out.println("20 - 3 = "+ (a-b));
		System.out.println("20 * 3 = "+ (a*b));
		System.out.println("20 / 3 = "+ (a/b));
		System.out.println("20 % 3 = "+ (a%b));
		
	}

}
