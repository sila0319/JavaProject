package _첫번째과제;

public class _5번문제 {
	public static void main(String[] args) {
		double firstsum = 10;
		double secondsum = 3.14;
		
		System.out.printf("%.0f + %.1f = %.0f %n",firstsum,secondsum,firstsum+secondsum);
		System.out.printf("%.0f - %.1f = %.0f %n",firstsum,secondsum,firstsum-secondsum);
		System.out.printf("%.0f / %.1f = %.0f %n",firstsum,secondsum,firstsum*secondsum);
		System.out.printf("%.0f * %.1f = %.0f %n",firstsum,secondsum,firstsum/secondsum);
	}

}
